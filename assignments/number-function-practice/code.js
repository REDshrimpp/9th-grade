//sum
const sum = (number1, number2) => {
  return number1 + number2
}

//averageOfThree
const averageOfThree = (number1, number2, number3) => {
  return (number1 + number2 + number3) / 3
}

//areaOfATrapazoid
const areaOfATrapazoid = (base1, base2, height) => {
  return 1 / 2 * (base2 + base1) * height
}